4. **按优先级举例诠释**：
   - 必说最出名的招牌（不说显得不全）。
   - 优点覆盖多方面，别全堆在一个点（价格+体验+便利+特色分散讲）。
   - 长度决定数量：长→多讲几个方面；短→一方面一个例子；再短→砍不重要的。
